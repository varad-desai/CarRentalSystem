    private void loginJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginJButtonActionPerformed
        // TODO add your handling code here:
        
        if(emailidLoginJTextField.getText().isEmpty()){
            validationJLabel.setText("Email Id cannot be Empty!");
        }
        else if(passwordLoginJTextField.getText().isEmpty()){
            validationJLabel.setText("Password cannot be Empty!");
        }
        else if(isEmailNotValid(emailidLoginJTextField.getText())){
            validationJLabel.setText("Email is not Valid!");
        }
        else if(isPasswordNotValid(passwordLoginJTextField.getText())){
            validationJLabel.setText("Password is not Valid!");
        }
        else{
            AbstractCarRentalSystemAPI acra = AbstractCarRentalSystemAPI.getObject();
            AbstractPersonAPI person = null;
            if(type.equalsIgnoreCase("Lessee")) {
                person = acra.getLessee(emailidLoginJTextField.getText(), passwordLoginJTextField.getText());
                if(person != null) {
                    this.dispose();
                    LesseeBookingJFrame lbjf = new LesseeBookingJFrame(person);
                    lbjf.setVisible(true);
                }
            } else if(type.equalsIgnoreCase("Renter")) {
                person = acra.getRenter(emailidLoginJTextField.getText(), passwordLoginJTextField.getText());
                if(person != null) {
                    this.dispose();
                    RenterHomeJFrame rhjf = new RenterHomeJFrame(person);
                    rhjf.setVisible(true);
                }
            } else if(type.equalsIgnoreCase("System")) {
                person = acra.getSystemAdmin(emailidLoginJTextField.getText(), passwordLoginJTextField.getText());
                if(person != null) {
                    this.dispose();
                    SystemHomeJFrame shjf = new SystemHomeJFrame(person);
                    shjf.setVisible(true);
                }
            }

            if(person == null) {
                System.out.println("Wrong credentials");
                JOptionPane.showMessageDialog(null, type + " Wrong credentials!");
                return;
            }
            
            validationJLabel.setForeground(Color.blue);
            validationJLabel.setText("Login Successful!");
        }
        
    }//GEN-LAST:event_loginJButtonActionPerformed

