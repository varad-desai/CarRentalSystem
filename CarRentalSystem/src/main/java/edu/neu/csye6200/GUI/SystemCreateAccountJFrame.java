    private void driverCreateAccountJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_driverCreateAccountJButtonActionPerformed
        // TODO add your handling code here:
        DateFormat dateFormatter = new SimpleDateFormat("MM/dd/yyyy");
        
        if(systemNameJTextField.getText().isEmpty()){
            validationJLabel.setText("Name field cannot be Empty!");
        }
        else if(systemDOBLoginJTextField1.getText().isEmpty()){
            validationJLabel.setText("Date of birth cannot be Empty!");
        }
        else if(systemPhoneNoJTextField1.getText().isEmpty()){
            validationJLabel.setText("Phone number cannot be Empty!");
        }
        else if(systemEmailidJTextField3.getText().isEmpty()){
            validationJLabel.setText("Email Id cannot be Empty!");
        }
        else if(systemPasswordJTextField3.getText().isEmpty()){
            validationJLabel.setText("Password cannot be Empty!");
        }
        else if(isDateNotValid(systemDOBLoginJTextField1.getText())){
            validationJLabel.setText("Date of Birth not Valid!");
        }
        else if(isEmailNotValid(systemEmailidJTextField3.getText())){
            validationJLabel.setText("Email is not Valid!");
        }
        else if(isPasswordNotValid(systemPasswordJTextField3.getText())){
            validationJLabel.setText("Password is not Valid!");
        }
        else{
            StringBuilder sb = new StringBuilder();

            sb.append(systemNameJTextField.getText()).append(",");
            sb.append(systemDOBLoginJTextField1.getText()).append(",");
            sb.append(systemPhoneNoJTextField1.getText()).append(",");
            sb.append(systemEmailidJTextField3.getText()).append(",");
            sb.append(systemPasswordJTextField3.getText());

            String personData = sb.toString();

            AbstractCarRentalSystemAPI acra = AbstractCarRentalSystemAPI.getObject();
            acra.addSystemAdmin(personData);
            this.dispose();
            CreateAccountLoginJFrame_new cajf = new CreateAccountLoginJFrame_new("System");
            cajf.setVisible(true);
        }
    }//GEN-LAST:event_driverCreateAccountJButtonActionPerformed

