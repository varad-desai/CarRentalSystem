    final static String DATE_FORMAT = "MM/dd/yyyy";

    /**
     * Date format: "MM/dd/yyyy"
     * @param date
     * @return 
     */
    public boolean isDateNotValid(String date) 
    {
            try {
                DateFormat df = new SimpleDateFormat(DATE_FORMAT);
                df.setLenient(false);
                df.parse(date);
                return false;
            } catch (ParseException e) {
                return true;
            }
    }
    
    /**
     * Email format: something@something.com
     * @param email
     * @return 
     */
    public boolean isEmailNotValid(String email){
        String regex = "^(.+)@(.+)$";//"^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";
        boolean result;
        if(email.matches(regex)){
            result = false;
        }
        else {
            result = true;
        }
        return result;
    }
    
    /**
     * Password constraints:
     * 1. It contains at least 8 characters and at most 20 characters.
     * 2. It contains at least one digit.
     * 3. It contains at least one upper case alphabet.
     * 4. It contains at least one lower case alphabet.
     * 5. It contains at least one special character which includes !@#$%&*()-+=^.
     * 6. It doesnâ€™t contain any white space.
     * @param password
     * @return 
     */
    public boolean isPasswordNotValid(String password){
        String regex = "^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])(?=.*[@#$%^&-+=()])(?=\\S+$).{8,20}$";

        boolean result;
        if(password.matches(regex)){
            result = false;
        }
        else{
            result = true;
        }
        return result;
    }
    

