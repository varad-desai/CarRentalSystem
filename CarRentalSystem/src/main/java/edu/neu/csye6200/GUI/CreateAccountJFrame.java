    private void lesseeCreateAccountJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lesseeCreateAccountJButtonActionPerformed
        // TODO add your handling code here:
        
        DateFormat dateFormatter = new SimpleDateFormat("MM/dd/yyyy");
        
        if(lesseeNameJTextField.getText().isEmpty()){
            validationJLabel.setText("Name field cannot be Empty!");
        }
        else if(lesseeLicenseNumberJTextField.getText().isEmpty()){
            validationJLabel.setText("License number cannot be Empty!");
        }
        else if(lesseeDateOfBirthJTextField.getText().isEmpty()){
            validationJLabel.setText("Date of birth cannot be Empty!");
        }
        else if(lesseePhoneNoJTextField.getText().isEmpty()){
            validationJLabel.setText("Phone number cannot be Empty!");
        }
        else if(lesseeEmailidJTextField.getText().isEmpty()){
            validationJLabel.setText("Email Id cannot be Empty!");
        }
        else if(lesseePasswordJTextField.getText().isEmpty()){
            validationJLabel.setText("Password cannot be Empty!");
        }
        else if(isDateNotValid(lesseeDateOfBirthJTextField.getText())){
            validationJLabel.setText("Date of Birth not Valid!");
        }
        else if(isEmailNotValid(lesseeEmailidJTextField.getText())){
            validationJLabel.setText("Email is not Valid!");
        }
        else if(isPasswordNotValid(lesseePasswordJTextField.getText())){
            validationJLabel.setText("Password is not Valid!");
        }
        else{
            
            validationJLabel.setText("");
            
            StringBuilder sb = new StringBuilder();

            sb.append(lesseeNameJTextField.getText()).append(",");
            sb.append(lesseeLicenseNumberJTextField.getText()).append(",");
            sb.append(lesseeDateOfBirthJTextField.getText()).append(",");
            sb.append(lesseePhoneNoJTextField.getText()).append(",");
            sb.append(lesseeEmailidJTextField.getText()).append(",");
            sb.append(lesseePasswordJTextField.getText());

            String personData = sb.toString();

            AbstractCarRentalSystemAPI acra = AbstractCarRentalSystemAPI.getObject();

            if(type.equalsIgnoreCase("Lessee")) {
                acra.addLessee(personData);
            } else if(type.equalsIgnoreCase("Renter")) {
                acra.addRenter(personData);
            }
            
            validationJLabel.setForeground(Color.blue);
            validationJLabel.setText(type+" Account Sucessfully Created!");

            JOptionPane.showMessageDialog(null, "Account created successfully.");
            this.dispose();
            HomeJFrame hjf = new HomeJFrame();
            hjf.setVisible(true);

        }
    }//GEN-LAST:event_lesseeCreateAccountJButtonActionPerformed

