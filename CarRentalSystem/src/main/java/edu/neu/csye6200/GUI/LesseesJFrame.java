    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(lesseesTable.getModel().getRowCount() == 0 || lesseesTable.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Please select a lessee to delete.");
            return;
        }
        
        int option = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete?");
        
        if(option == 2 || option == 1)
            return;
        
        UUID lesseeId = (UUID) lesseesTable.getValueAt(lesseesTable.getSelectedRow(), 6);
        
        AbstractCarRentalSystemAPI.getObject().deleteLessee(lesseeId);
        
        JOptionPane.showMessageDialog(null, "Lessee deleted successfully");
        this.dispose();
        LesseesJFrame ljf = new LesseesJFrame(person);
        ljf.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

