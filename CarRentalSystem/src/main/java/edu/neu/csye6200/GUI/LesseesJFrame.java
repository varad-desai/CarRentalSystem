    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) lesseesTable.getModel();
        model.setRowCount(0);
        AbstractCarRentalSystemAPI
                .getObject()
                .getAllLessees().stream()
                    .forEach(person -> {
                        Lessee l = (Lessee) person;
                        Object[] row = new Object[7];
                        row[0] = l.getName(); 
                        row[1] = l.getLicenseNumber();
                        row[2] = l.getDateOfBirth();
                        row[3] = l.getPhoneno();
                        row[4] = l.getEmailId();
                        row[5] = l.getPassword();
                        row[6] = l.getPersonId();
                        model.addRow(row);
                    });
        lesseesTable.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
