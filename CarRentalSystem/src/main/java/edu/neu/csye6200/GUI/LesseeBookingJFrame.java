    public void loadCarTable() {
        DefaultTableModel model = (DefaultTableModel) carListAll.getModel();
        model.setRowCount(0);
        AbstractCarRentalSystemAPI
                .getObject()
                .getAllCars().stream()
                    .forEach(car -> {
                        Object[] row = new Object[3];
                        row[0] = car;
                        row[1] = car.getName();
                        row[2] = car.getRentPerHour();
                        model.addRow(row);
                    });
        carListAll.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
    
    public void loadBookingTable() {
        DefaultTableModel model = (DefaultTableModel) bookingList.getModel();
        model.setRowCount(0);
        AbstractCarRentalSystemAPI
                .getObject()
                .getAllBookings().stream()
                    .filter(booking -> booking.getLesseeId().equals(this.person.getPersonId()))
                    .forEach(booking -> {
                        AbstractCarAPI bookedCar = AbstractCarRentalSystemAPI.getObject()
                                .getCar(booking.getCarId()); 
                        Object[] row = new Object[6];
                        row[0] = bookedCar;
                        row[1] = bookedCar.getName();
                        row[2] = booking;
                        row[3] = booking.getDate();
                        row[4] = booking.getStartTime();
                        row[5] = booking.getEndTime();
                        model.addRow(row);
                    });
        bookingList.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }
