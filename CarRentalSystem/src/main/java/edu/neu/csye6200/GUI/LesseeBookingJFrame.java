    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(carListAll.getModel().getRowCount() == 0 || carListAll.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Please select a car to book.");
            return;
        }
        else if(bookingDate.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter a date to book.");
            return;
        }
        else if(startBookingTime.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter start time to book.");
            return;
        }
        else if(endBookingTime.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter end time to book.");
            return;
        }
        else if(isDateNotValid(bookingDate.getText())) {
            JOptionPane.showMessageDialog(null, "Date is not valid.");
            return;            
        } // add for isTimeNotValid - startTime and endTime as well
        
        AbstractCarAPI selectedCar = (AbstractCarAPI) carListAll.getValueAt(carListAll.getSelectedRow(), 0);
        
        StringBuilder sb = new StringBuilder();
        sb.append(this.person.getPersonId()).append(",");
        sb.append(selectedCar.getCarId()).append(",");
        sb.append(bookingDate.getText()).append(",");
        sb.append(startBookingTime.getText()).append(",");
        sb.append(endBookingTime.getText());
        
        try {
            AbstractCarRentalSystemAPI.getObject().addBooking(sb.toString());
            JOptionPane.showMessageDialog(null, "Booking added successfully!"); 
            this.dispose();
            LesseeBookingJFrame lbjf = new LesseeBookingJFrame(person);
            lbjf.setVisible(true);
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Some error occurred!");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

